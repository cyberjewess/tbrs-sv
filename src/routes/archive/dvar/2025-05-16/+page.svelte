	<h1>Parshat Emor</h1>
	<h2>Francesca R</h2>
	<h3>May 16, 2025</h3>

	<p>
		Hello everyone, I'm Francesca. I've enjoyed getting to be a part of the Bushwick-Ridgewood shul
		community over the last few months, thanks for welcoming me tonight. Just about a year ago, I
		received my grad degree in Jewish studies and moved to New York. I'm grateful to have the chance
		to share a dvar torah for the first time in a while and hope I'm not too rusty.
	</p>

	<p>
		So here goes: Parshat Emor, Leviticus 21-24. When I spoke to Esti about writing a dvar torah or
		mini-sermon tonight, I'll admit I'd lost track of where exactly we were in the Hebrew Bible. My
		favorite biblical texts are the juicy, more character driven narratives of Genesis and Exodus,
		or the often dark and politically complicated stories of Judges and Kings.
	</p>

	<p>
		Leviticus is another story. Much Leviticus is concerned with the ritual and sacrificial
		practices connected to the kohanim, the priestly descendants of Aaron who offered sacrifices to
		God first in the desert and then in more permanent holy places like the Temples.
	</p>

	<p>
		Leviticus gets into the weeds about the proper animal and grain sacrifices that the priests
		should make, in the time before rabbinic Judaism replaced burnt offerings with intangible
		prayers. There is also a deep concern in Leviticus about ritual purity, what makes people
		"tahor," and "tamei." These Hebrew terms are traditionally translated as "tahor: pure, clean,
		sacred" and "tamei: impure, contaminated, profane." However, some translators offer more neutral
		understandings of "tamei" and "tahor." This is important especially to feminist scholars because
		menstruation is one of the states or actions that supposedly makes one "tamei."
	</p>

	<p>
		Rather than purity and impurity, feminist commentators prefer to describe these states as
		"ready" and "unready," and remind us that one's ritual status can be easily changed by the dip
		in the mikveh or ritual bath, or really any body of running water. Being "tamei" or ritually
		impure isn't a moral judgement. Rather it's a fact of life that can be easily moved through or
		adjusted. However, many translators and readers of our sacred texts still understand ritual
		impurity to have a negative connotation.
	</p>

	<p>
		This is all context for discussing a relatively insignificant passage early in Parshat Emor that
		drew my attention.
	</p>

	<p>
		Leviticus 21:1 reads, "Speak to the priests, the sons of Aaron, and say to them: None shall
		defile himself [literally, make himself tamei] for any [dead] person among his kin, except for
		the relatives that are closest to him: his mother, his father, his son, his daughter, and his
		brother; also for a virgin sister."
	</p>

	<p>
		Contact with a dead body is one of the actions that makes someone tamei, ritually impure or
		unready, according to Leviticus. This passage is telling the priests that they are only allowed
		to be in the same space as a dead person, or help with preparing them for burial, when that
		person is a blood family member.
	</p>

	<p>
		In Leviticus 21:11, this strict instruction around ritual purity is repeated and intensified for
		the high priest. The verse reads, "The priest who is exalted among his fellows...he shall not go
		in where there is any dead body; he shall not defile himself [again become tamei] even for his
		mother and father."
	</p>

	<p>
		These passages made me sad, made me want to push back and critique, which of course is a major
		part of biblical interpretation. Parshat Emor places such a high value on the tahor-ness, the
		ritual purity of the priests, that they restrict them from participating in mourning practices
		for friends, extended family, teachers or mentors.
	</p>

	<p>
		The high priest can't sit with his parents' bodies after they pass away, supposedly to remain
		ritually pure and ready for the Holy One even in the face of devastating loss. In a post-Covid
		world, this passage conjures reminders of people who said goodbye to loved ones through PPE and
		Zoom screens, restrictions on visitors and virtual shivas.
	</p>

	<p>
		I reacted strongly to these priestly laws, and wanted to unpack them here with you, because of
		these memories, but also because we live in a time of increasing loneliness, isolation. We are
		also seeing authoritarianism rise around the world, which thrives in an environment lacking
		trust and vibrant, diverse communities.
	</p>

	<p>
		In the great queer and feminist tradition, I invite us to trouble and question Leviticus 21. We
		aren't priests, or even if we may be kohanim, we don't connect with God in the same way the
		ancient Israelites did, keeping track of our ritual purity in the services of burnt offerings.
		We can question what defilement means, what closeness means.
	</p>

	<h3>
		I think it is important to ask ourselves, who will I choose to care for and comfort, other than
		my parents and children and siblings? Who am I willing to get my hands dirty for? Who will I
		trangress for, or transgress with? How can I break out of the isolation and restrictions placed
		on me, to help build a stronger community and a better world? How can I get myself ready to do
		that work?
	</h3>

	<p>
		I'm coming away from this Torah portion with more questions than answers, but I hope you'll
		wrestle with them alongside me. Thank you!
	</p>
