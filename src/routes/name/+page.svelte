	<h3>Beth Kherut Spinoza - בית חירות שפינוזה</h3>
	<ul>
		<li>Suggested by Alaska</li>
		<li>
			<p>
				Brauch Spinoza was a noted Jewish intellectual, dissident, and philosopher who
				excommunicated from the Sephardic Jewish community in Amsterdam that he grew up in on July
				27th, 1656, due to vocal disagreements he had with the local Jewish community that the
				rabbinical establishment of Amsterdam declared as "abominable heresies", as they felt his
				opinions were threatening to the narratives that where using for religious and social
				cohesion within. His expulsion came about for a variety of factors, but amongst the most
				important was due to his belief that the Torah was not divinely gifted in its entirely on
				mount Saini, and instead was authored by humans, as well as rejecting the concept of a
				personal god, as well as refuting ideas regarding the unique chosenness of Jews in the eyes
				of God.
			</p>
			<p>
				Spinoza's beliefs are not dissimilar modern day humanistic Judaism and where considered
				highly controversial in his Sephardic community that at the time was facing a large influx
				of refugee Jews fleeing the Spanish inquisition, leading to an increase in political
				instability within the region, thus eliciting a harsher response by community leaders to
				Spinoza's dissenting theological opinions.
			</p>
			<p>
				All of this of course, begs the question as to why it would make sense to name a shul after
				a man noted as a heretic by his own community during his lifetime. My agreement for this is
				that in using Spinoza in our name, we can, in the way that this shul refers to itself,
				embody a similar spirit of dissent from current Jewish cultural norms regarding Zionism, a
				dissent that in many ways similar to Spinoza's, as in both cases our dissent alienates us
				from much of the mainstream Jewish community, but will likely retrospectively be considered
				an important movement within Judaism.
			</p>
			<p>
				In particular, I think there is also a degree of metaphorical alignment between Spinoza's
				premise that Jewish people, and humans more broadly, are not specifically privileged in the
				eyes of God, and anti-Zionism, which in its modern for is in many ways is derived from the
				premise that Jews are not more entitled to exist in the land of Palestine any more than the
				Palestinians who already lived there. In addition to this, Spinoza greatly advocated for a
				focus on ethics and living an ethical life, which is another important alignment his views
				have with anti-Zionism.
			</p>
		</li>
	</ul>
